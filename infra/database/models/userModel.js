/**
 * User Schema
 * This file describes the User Model
 * 
 * @module UserSchema
 */

import mongoose from 'mongoose';

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export const UsersModel = mongoose.model('User', UserSchema);